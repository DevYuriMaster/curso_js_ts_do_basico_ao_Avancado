let profissao = "Policial";

console.log("Profissão: " + profissao);

switch (profissao) {
    case 'Policial':
        console.log('Sua camisa será Azul');
        break;
    case 'Bombeiro':
        console.log('Sua camisa será Vermelha');
        break;
    case 'Fiscal':
        console.log('Sua camisa é Verde');
        break;
    default:
        console.log('Sua camisa é Preta');
        break;
} 







